    private void cortar(int fila, int columna, int ancho, int alto){
        for (int i = 0, k = columna; i < alto; i++, k++) {
            for (int j = 0, l = fila; j < ancho; j++, l++) {
                Border border = BorderFactory.createLineBorder(Color.WHITE, 2, false);
                this.matrizBotones[k][l].setBackground(this.p.getFilaColores(k).get(l));
                this.matrizBotones[k][l].setBorder(border); 
            }
        }

    }
    
