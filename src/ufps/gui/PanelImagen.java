    private void crear() {
        try {

            int n = this.p.getLength();
            int m = this.p.getFilaColores(0).getLength();

            int x;
            int y;
            this.matrizBotones = new JTextField[n][m];

            for (int i = 0; i < n; i++) {
                x = (((60-n)/2)+(i*1));
                for (int j = 0; j < m; j++) {
                    y = (((70-m)/2)+(j*1));
                    Border border = BorderFactory.createLineBorder(this.p.getFilaColores(i).get(j), 2, false);
                    
                    this.matrizBotones[i][j] = new JTextField("");
                    this.matrizBotones[i][j].setBounds((y*10), (x*10), 10, 10);
                    this.matrizBotones[i][j].setBorder(border);
                    
                    this.matrizBotones[i][j].setCursor(null);
                    this.matrizBotones[i][j].setEditable(false);
                    this.matrizBotones[i][j].setBackground(this.p.getFilaColores(i).get(j));
                    this.add(this.matrizBotones[i][j]);
                    if (i==n-1) {
                        this.panelLoading.setVisible(false);
                    }
                }
            }
            this.repaint();
        } catch (java.lang.NumberFormatException e) {
            throw new RuntimeException("Error de entrada de datos");
        } catch (java.lang.NegativeArraySizeException e) {
            throw new RuntimeException("cantidad<0");
        }

    }
